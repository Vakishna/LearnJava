
// Programming Objects Practical Example

public class Person {

    String firstName = "Vakishna";
    String lastName = "Thayalan";
    int age = 21;


    public static void main(String[] args) {
        Person Vak = new Person();
        System.out.println("Full name: " + Vak.firstName + " " + Vak.lastName);
        System.out.println("Age: " + Vak.age);
    }




}
