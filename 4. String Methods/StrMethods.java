public class StrMethods {


    public static void main(String[] args) {
        String txt = "This is a line of text, declared to a Variable named txt of type String.";
        System.out.println("txt: " + txt);
        System.out.println("txt uppercase: " + txt.toUpperCase());
        System.out.println("txt lowercase: " + txt.toLowerCase());
        System.out.println("Index of word \"Named\" in txt: " + txt.indexOf("named"));
    }

}
